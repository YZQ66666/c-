#include<iostream>
using namespace std;
int main()
{
	int a;
	cin >> a;
	if (a % 2 == 0)
	{
		cout << "a是偶数" << endl;
	}
	else
	{
		cout << "a是奇数" << endl;
	}
	return 0;
}